#include <player.h>

Player::Player(PROBTYPE _probs[STONES]) {
    if (_probs == 0) {
        for (int i = 0; i < STONES; i++) {
            probs[i] = 1./6.;
        }
    } else {
        for (int i = 0; i < STONES; i++) {
            probs[i] = _probs[i];
        }
    }
}
